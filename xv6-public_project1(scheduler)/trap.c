// Interrupt descriptor table (shared by all CPUs).
struct gatedesc idt[256];
extern uint vectors[];  // in vectors.S: array of 256 entry pointers
struct spinlock tickslock;
uint ticks;

void
 tvinit(void)
{
  int i;

  for(i = 0; i < 256; i++)
  //DPL
    SETGATE(idt[i], 0, SEG_KCODE<<3, vectors[i], 0);
  SETGATE(idt[T_SYSCALL], 1, SEG_KCODE<<3, vectors[T_SYSCALL], DPL_USER);
  SETGATE(idt[T_MYCALL], 0, SEG_KCODE<<3, vectors[T_MYCALL], DPL_USER);

  // user mode에서도 interrupt를 발생시킬 수 있게 함
  SETGATE(idt[T_SCHEDULERLOCK], 0, SEG_KCODE<<3, vectors[T_SCHEDULERLOCK], DPL_USER);
  SETGATE(idt[T_SCHEDULERUNLOCK], 0, SEG_KCODE<<3, vectors[T_SCHEDULERUNLOCK], DPL_USER);
  initlock(&tickslock, "time");
}

void
idtinit(void)
{
  lidt(idt, sizeof(idt));
}

